void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}
int main()
{
	int a = 65;
	int b = 32;
	printf("%d \n %d \n",a,b);
	ft_swap(&a, &b);
	printf("%d \n %d \n", a, b);

}
