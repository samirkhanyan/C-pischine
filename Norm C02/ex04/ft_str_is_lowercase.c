int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	tmp;

	i = 0;
	while (str[i] != '\0')
	{
		tmp = str[i];
		if (tmp <= 96 || tmp >= 123)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
