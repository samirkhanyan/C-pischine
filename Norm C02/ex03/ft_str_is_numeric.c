int	ft_str_is_numeric(char *str)
{
	int	i;
	int	tmp;

	i = 0;
	while (str[i] != '\0')
	{
		tmp = str[i];
		if (tmp <= 47 || tmp >= 58)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
